# los operadores de comparacion comparan datos. Devuelven true o false

# operador igual (==)

igual_que = 5 == 6 #solo un igual es de asignacion, para comparar se usan dos.

# distinto (!=)

distinto_de = 5 != 6

# mayor & menor (> & <)

mayor_que = 5 > 6

menor_que = 5 < 6

# mayor/menor o igual (>= & <=)

mayor_o_igual = 5 >= 6

menor_o_igual = 5 <= 6

print(igual_que, distinto_de, mayor_que, menor_que, mayor_o_igual, menor_o_igual)