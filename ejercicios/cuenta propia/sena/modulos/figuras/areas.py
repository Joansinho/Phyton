#Area cuadrado
def area_cuadrado(lado):
    return lado * lado

#Area rectangulo
def area_rectangulo(base, altura):
    return base * altura

#Area triangulo
def area_triangulo(base, altura):
    return (base * altura) / 2

#Area rombo
def area_rombo(diagonal_mayor, diagonal_menor):
    return (diagonal_mayor * diagonal_menor)