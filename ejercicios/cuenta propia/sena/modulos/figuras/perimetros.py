#Paquete borde

#Perimetro cuadrado
def perimetro_cuadrado(lado):
    return 4 * lado

#Perimetro rectangulo
def perimetro_rectangulo(base, altura):
    return 2 * (base + altura)

#Perimetro triangulo
def perimetro_triangulo(lado):
    return 3 * lado

#Perimetro rombo
def perimetro_rombo(lado):
    return 4 * lado

